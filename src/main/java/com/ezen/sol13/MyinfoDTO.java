package com.ezen.sol13;

public class MyinfoDTO {
	
	int bun;
	String name;
	int age;
	String address;
	MyscoreDTO myscoreDTO;
	
	public MyinfoDTO() {}

	public int getBun() {
		return bun;
	}

	public void setBun(int bun) {
		this.bun = bun;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public MyscoreDTO getMyscoreDTO() {
		return myscoreDTO;
	}

	public void setMyscoreDTO(MyscoreDTO myscoreDTO) {
		this.myscoreDTO = myscoreDTO;
	}



	
	
}
